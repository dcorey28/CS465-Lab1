package main

import (
	"fmt"

	"github.com/dcorey28/CS465/Lab1/aes"
)

func main() {
	fmt.Println("AES-128(Nk=4, Nr=10)")
	runCipher128()
	runInvCipher128()

	fmt.Println("AES-192(Nk=6, Nr=12)")
	runCipher192()
	runInvCipher192()

	fmt.Println("AES-256(Nk=8, Nr=14)")
	runCipher256()
	runInvCipher256()
}

func runCipher128() {
	in := []byte{0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff}
	key := []byte{0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f}

	fmt.Printf("PLAINTEXT:         %x\n", in)
	fmt.Printf("KEY:               %x\n\n", key)

	aes.Encrypt(in, key)
}

